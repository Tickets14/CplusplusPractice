#include <iostream>

int main() {
    for(int i = 1; i<=10; i++) {
        std::cout << 'A';
    }

    for(int j = 1; j<=7; j++) {
        std::cout << 'B';
    }

    for(int k = 1; k<=4; k++) {
        std::cout << "CD";
    }

    std::cout << 'E';

    for(int l = 1; l<=6; l++ ) {
        std::cout << 'F';
    }

    std::cout << 'G';
}